	'defaultLineLineWidth', 3, ...
	'defaultAxesLineWidth', 2, ...
	'defaultAxesFontSize', 9, ...
	'defaultAxesFontWeight', 'bold', ...
	'defaultAxesFontName', 'nimbus sans', ...
	'defaultFigureUnits', 'inches', ...
    'defaultFigurePosition', [6.0972    5.2361    7.7778    5.8333], ...
	'defaultAxesOuterPosition', [0 0 1 1])
end
