function style = set_style()
cmap = lines(7);
style.M.color = cmap(1, :);
style.E.color = cmap(2, :);
style.D10.color = cmap(3, :);
style.D1.color = cmap(5, :);
style.D1xwh.color = cmap(4, :);
for ff = string(fieldnames(style)')
    style.(ff).displayname = ff; 
end

